<p class="primeiro">
	Os cachorros estão presentes na vida das pessoas há bastante tempo, já sendo parte essencial de
	todo o funcionamento da sociedade humana. Portanto, os cães são utilizados para inúmeras tarefas
	atualmente, mas no passado esse número era ainda maior. Isso porque, como todos os trabalhos eram
	muito mais manuais, sem o auxílio de tanta tecnologia, era preciso fazer uso da força e da
	inteligência animal para conseguir chegar os objetivos.
</p>

<p>
	Logo, com o tempo as pessoas foram realizando cruzamentos artificiais para criar novas raças de
	cães, buscando sempre chegar ao tipo de cachorro desejado, com as principais características úteis
	àquele trabalho determinado. Foi assim que nasceu o border collie, um dos cães mais inteligentes
	de todo o mundo e que, atualmente, faz parte da longa lista de cachorros usados como cães de
	companhia. O border collie, assim, nasceu para servir como pastor de ovelhas.
</p>

<p>
	Contudo, se as pessoas já não possuem mais tantas ovelhas quanto no passado, a utilidade do animal
	foi sendo alterada com o passar do tempo. É possível dizer que o border collie, assim, teve o seu
	papel na sociedade bastante alterado com o transcorrer das décadas, algo que muitas outras raças
	também puderam acompanhar.
</p>

<p>
	O border collie está entre os animais mais inteligentes de todo o mundo, mas, ainda assim, esse
	cachorro era usado apenas como pastor de ovelhas no passado. Isso porque, por ser bastante
	obediente, o border collie oderia ser utilizado para impedir que animais se perdessem, sendo mais
	eficiente do que outras raças de cães para o serviço.
</p>

<p>
	De toda forma, foi para isso que surgiu o border collie, após uma série de cruzamentos, que só
	tiveram o seu primeiro estabelecido em 1906. Com cerca de 100 anos de ajustes, a raça já havia
	começado a ser “fabricada” em 1800, mas demorou bastante até que as pessoas pudessem acertar os
	padrões e definir quais cachorros poderiam ser tratados como o border collie e quais não deveriam
	ser assim chamados. Não demorou nada para que o animal, de origem britânica, pudesse chegar ao
	continente sul-americano.
</p>

<p>
	Com uma economia muito baseada nos trabalhos ligados ao campo, o border collie rapidamente pôde
	ser usado como pastor também na América do Sul. Esse animal, com o passar do tempo e com a sua
	inteligência sendo notada pelas pessoas, rapidamente ganhou fama em todo o continente, incluindo o
	Brasil.
</p>

